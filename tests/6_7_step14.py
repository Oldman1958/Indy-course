letters = {
    'h': [0],
    'e': [1],
    'l': [2, 3, 9],
    'o': [4, 7],
    ' ': [5],
    'w': [6],
    'r': [8],
    'd': [10]
}

len_sentence = 0
for value in letters.values():
    len_sentence += len(value)

sentence = ['*'] * len_sentence

for key, value in letters.items():
    for i in value:
        sentence[i] = key
print(''.join(sentence))
